#include "libft.h"

char	*ft_strnstr(const char *str, const char *substr, size_t n)
{
	char	*pos;

	if (!*substr)
		return ((char *) str);
	pos = ft_strstr(str, substr);
	if (pos == NULL)
		return (NULL);
	if (pos - str + ft_strlen(substr) <= n)
		return (pos);
	return (NULL);
}
